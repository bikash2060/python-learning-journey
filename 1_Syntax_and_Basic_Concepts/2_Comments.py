# Comments in Python
"""
    - Comments are line of text or notes written in a program to explain the code that are ignored by the Python interpreter.
    - They are written to explain code, make it more easier to understand, and help others (or yourself) when you read the code later.
    - Basically, comments are for humans, not for computers.
"""

# Types of Comments in Python
"""
    1. Single-line Comments: These comments start with a hash symbol (#) and extend to the end of the line.
    2. Multi-line Comments: These comments are enclosed within triple quotes (''' or """ """) and can span multiple lines.
"""

# Example of Single-line Comment
print("Hello, This is a single-line comment example.")  # This is a single-line comment using the hash symbol

# Example of Multi-line Comment
"""
    Multi-line comments are also called docstrings (Documentation strings).
    This is a multi-line comment example.
    It can span multiple lines.
    Useful for longer explanations or documentation.
    Multi-line comments are created using triple quotes and need to be closed with triple quotes.
"""
print("Hello, This is a multi-line comment example.")